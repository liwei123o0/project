class ProxyMiddleware(object):

    def spider_opened(self, spider):

        self.enabled = spider.proxy.get('enabled', True)
        if not self.enabled:
            return
        self.rate = 10
    @classmethod
    def loadproxy(self):

        html = urllib2.urlopen("http://10.6.2.124/proxy/proxy.txt").read()
        return html

    def process_request(self, request, spider):
        html =  ProxyMiddleware.loadproxy()
        proxyes = re.split(r'\n',html)
        proxys = []
        for proxy1 in proxyes:
            proxy1 = proxy1.replace("http\t","http://").replace("\t",":")
            proxys.append(proxy1)
        proxy = random.choice(proxys)
        request.meta['proxy'] = proxy

        logging.log(logging.INFO,u"proxy:%s"%request.meta['proxy'])
